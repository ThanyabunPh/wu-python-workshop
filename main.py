color = ['Red', 'Green', 'White', 'Black', 'Pink', 'Yellow']

# แสดงจำนวนข้อมูลในลิสต์
n = len(color)
print(f"จำนวนข้อมูลในลิสต์: {n}")

# รับค่าตัวเลขจากผู้ใช้ โดยค่าตัวเลขต้องไม่เกินครึ่งหนึ่งของจำนวนข้อมูลในลิสต์
while True:
    user_input = int(input(f"กรุณาใส่ค่าตัวเลข (ไม่เกิน {n//2}): "))
    if 1 <= user_input <= n // 2:
        break
    else:
        print("ค่าที่คุณใส่ไม่ถูกต้อง กรุณาลองใหม่อีกครั้ง")

# แบ่งข้อมูลในลิสต์ให้เป็นลิสต์ย่อยที่มีจำนวนเท่ากับค่าที่ผู้ใช้ใส่
sub_lists = [color[i:i + user_input] for i in range(0, n, user_input)]

# แสดงลิสต์ย่อยที่ได้
print(f"ลิสต์ย่อยที่ได้จากการแบ่ง: {sub_lists}")
